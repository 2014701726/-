package whj.nb.motianluneureka.entity;


public class Coupon {

  private String couponId;
  private String couponName;
  private String couponTime;
  private String couponPrice;
  private String couponCon;
  private String couponType;
  private String couponState;
  private String customerId;


  public String getCouponId() {
    return couponId;
  }

  public void setCouponId(String couponId) {
    this.couponId = couponId;
  }


  public String getCouponName() {
    return couponName;
  }

  public void setCouponName(String couponName) {
    this.couponName = couponName;
  }


  public String getCouponTime() {
    return couponTime;
  }

  public void setCouponTime(String couponTime) {
    this.couponTime = couponTime;
  }


  public String getCouponPrice() {
    return couponPrice;
  }

  public void setCouponPrice(String couponPrice) {
    this.couponPrice = couponPrice;
  }


  public String getCouponCon() {
    return couponCon;
  }

  public void setCouponCon(String couponCon) {
    this.couponCon = couponCon;
  }


  public String getCouponType() {
    return couponType;
  }

  public void setCouponType(String couponType) {
    this.couponType = couponType;
  }


  public String getCouponState() {
    return couponState;
  }

  public void setCouponState(String couponState) {
    this.couponState = couponState;
  }

  public String getCustomerId() {
    return customerId;
  }

  public void setCustomerId(String customerId) {
    this.customerId = customerId;
  }
}
